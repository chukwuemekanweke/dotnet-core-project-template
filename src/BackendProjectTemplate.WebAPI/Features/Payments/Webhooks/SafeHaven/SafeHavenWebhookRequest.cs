public sealed record SafeHavenWebhookRequest(
    [property: JsonPropertyName("type")] string Type,
    JsonElement Data,
    [property: JsonPropertyName("eventType")] string? EventType = null)
{
    [JsonIgnore]
    public string Event => string.IsNullOrWhiteSpace(EventType) ? Type : EventType;
}

